package exo5.switchcase;

public class SwitchCase {
    public static void main(String args[]) {
        String color = "blue";
        switch (color) {
            case "blue":
                System.out.println("That's blue");
                break;
            case "red":
                System.out.println("That's red");
                break;
            default:
                System.out.println("That's red");
                break;
        }
    }
}